import type { Email } from '../types/email'

function dateParts(value: string) {
  const date = new Date(value)

  return {
    internalDate: String(date.getTime()),
    date: date.toLocaleString(undefined, {
      weekday: 'short',
      month: 'short',
      day: 'numeric',
      year: 'numeric',
      hour: 'numeric',
      minute: '2-digit',
    }),
    timestamp: date.toLocaleDateString(undefined, { month: 'short', day: 'numeric' }),
  }
}

function html(body: string) {
  return `<p>${body.replace(/\n{2,}/g, '</p><p>').replace(/\n/g, '<br>')}</p>`
}

function email(input: Omit<Email, 'provider' | 'date' | 'timestamp' | 'internalDate' | 'bodyHtml' | 'bodyPlain'> & { sentAt: string }): Email {
  const { sentAt, ...message } = input
  const parts = dateParts(sentAt)

  return {
    ...message,
    provider: 'custom',
    date: parts.date,
    timestamp: parts.timestamp,
    internalDate: parts.internalDate,
    importance: message.important ? 'high' : 'normal',
    bodyHtml: html(message.body),
    bodyPlain: message.body,
  }
}

